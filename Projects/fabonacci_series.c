int main()
{
	int digitone  = 0, digittwo = 1, calculated = 0, max_limit,i;
	printf("Enter the max limit for the fabonacci series:");
	scanf("%d",&max_limit);
	printf("%d, ",digitone);
	printf("%d, ", digittwo);
	
	for(i= 0;i<=max_limit;i++)
	{
		calculated = digitone + digittwo;
		printf("%d, ",calculated);
		digitone = digittwo;
		digittwo = calculated;
	}
	return 0;
}
